package com.hisun.bean;


public class FileItem
{
    private String file_id;
    private String file_name;
    private String file_type;
    private String file_path;
    private String file_length;
    private String update_staff;
    private String update_time;
    
    public String getUpdate_staff() {
		return update_staff;
	}

	public void setUpdate_staff(String update_staff) {
		this.update_staff = update_staff;
	}

	public String getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}

	public FileItem(){}
    
	public FileItem(String file_id, String file_name, String file_type,
			String file_path, String file_length) {
		super();
		this.file_id = file_id;
		this.file_name = file_name;
		this.file_type = file_type;
		this.file_path = file_path;
		this.file_length = file_length;
	}
	public String getFile_id() {
		return file_id;
	}
	public void setFile_id(String file_id) {
		this.file_id = file_id;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getFile_type() {
		return file_type;
	}
	public void setFile_type(String file_type) {
		this.file_type = file_type;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public String getFile_length() {
		return file_length;
	}
	public void setFile_length(String string) {
		this.file_length = string;
	}
    
    
    
    
}
